// ============================================================
// SPRITE REGISTRY
// ============================================================
const ALL_SPRITES = { neko: NEKO, piku: PIKU, robo: ROBO, blob: BLOB, drak: DRAK, octo: OCTO };

// Stage mapping
const STAGE_MAP = {
  egg: null, // uses EGG directly
  baby: 'baby',
  teen_happy: 'teen_happy',
  teen_neutral: 'teen_neutral',
  teen_sad: 'teen_sad',
  adult_angel: 'adult_angel',
  adult_normal: 'adult_normal',
  adult_gremlin: 'adult_gremlin',
  adult_secret: 'adult_secret',
};

/**
 * Get sprite frames for a species, stage, and state.
 * Returns an array of strings (frames).
 */
export function getSprite(species, stage, state) {
  if (stage === 'egg') {
    return EGG[state] || EGG.idle;
  }
  const speciesSprites = ALL_SPRITES[species];
  if (!speciesSprites) return EGG.idle;
  const stageKey = STAGE_MAP[stage];
  if (!stageKey || !speciesSprites[stageKey]) return EGG.idle;
  const stateSprites = speciesSprites[stageKey][state];
  if (!stateSprites) return speciesSprites[stageKey].idle || EGG.idle;
  return stateSprites;
}

export function getEggSprite(state) {
  return EGG[state] || EGG.idle;
}
